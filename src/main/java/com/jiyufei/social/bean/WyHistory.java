package com.jiyufei.social.bean;

import java.util.Date;

public class WyHistory {
    private Integer id;

    private Integer wyId;

    private Date time;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getWyId() {
        return wyId;
    }

    public void setWyId(Integer wyId) {
        this.wyId = wyId;
    }

    public Date getTime() {
        return time;
    }

    public void setTime(Date time) {
        this.time = time;
    }
}